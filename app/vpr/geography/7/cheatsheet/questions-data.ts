// ─── Simplified types (mirror @/lib/map-utils without hard dependency) ────────────
export type GeoBounds = { top: number; bottom: number; left: number; right: number };

export type PoiType = 'point' | 'path' | 'loop';

export interface MapPoint {
  id: string;
  name: string;
  type: PoiType;
  icon: string;
  color: string;
  coords: [number, number][];
  geojson?: Record<string, unknown>;
  roadHighlight?: { weight?: number; glow?: boolean; animated?: boolean; dashArray?: string };
}

// ─── Question types ──────────────────────────────────────────────────────────────
export type QuestionType = 'locate' | 'identify' | 'choice' | 'climate' | 'profile' | 'scale' | 'table';

export interface ProfileFeature {
  label: string;
  x: number;
  y: number;
  letter: string;
}

export interface TerrainProfile {
  /** SVG polyline path [x, y] in 0-100 normalised coords (0,0 = top-left) */
  path: [number, number][];
  /** Landmark features to label on the cross-section */
  features: ProfileFeature[];
  xLabel: string;
  yLabel: string;
  yTicks: number[];  // height labels on Y-axis (metres)
  xTicks: string[];  // distance labels on X-axis
}

export interface ScaleQuestion {
  /** Two cities / landmarks on the map */
  cityA: { name: string; coords: [number, number] };
  cityB: { name: string; coords: [number, number] };
  /** Correct straight-line distance in km */
  correctKm: number;
  /** Named scale shown on the map, e.g. "в 1 см — 200 км" */
  namedScale: string;
  /** Numerical scale denominator, e.g. 20_000_000 for 1:20 000 000 */
  numericalScale: number;
}

export interface TableRow {
  country: string;
  area: number;        // млн км²
  population: number;  // млн чел
}

export interface GeoTable {
  headers: string[];
  rows: TableRow[];
  question: string;
  correctAnswer: string;
  explanation: string;
}

export interface Question {
  id: number;
  vprType: string;
  type: QuestionType;
  text: string;
  options?: string[];
  correctAnswer: string | number | [number, number];
  tolerance?: number;
  mapConfig: {
    bounds: GeoBounds;
    points: MapPoint[];
    tileLayer?: 'cartodb-dark' | 'cartodb-light' | 'osm';
    interactionHint: string;
  };
  explanation: string;
  hint?: string;
  /** Terrain cross-section data for profile questions */
  profile?: TerrainProfile;
  /** Scale measurement data for scale questions */
  scaleInfo?: ScaleQuestion;
  /** Table data for table questions */
  tableInfo?: GeoTable;
}

